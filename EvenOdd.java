class EvenOdd
{
	public static void main(String[] args)
	{
		int a =5;
		String b=(a%2==0)?"5 is Even Numbers":"5 is Odd Numbers";
		System.out.println(b);

		int c =8;
		String d=(c%2==0)?"8 is Even Numbers":"8 is Odd Numbers";
		System.out.println(d);

		int e =24;
		String f=(e%2==0)?"24 is Even Numbers":"24 is Odd Numbers";
		System.out.println(f);

		int g =99;
		String h=(g%2==0)?"99 is Even Numbers":"99 is Odd Numbers";
		System.out.println(h);
	}
}