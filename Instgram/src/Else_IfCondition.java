
public class Else_IfCondition {
	public static void main (String args[]) {
		int a = 10;
		int b = 20;
		if (a == b) {
			System.out.println("a is equal to b");
		}
		else if (a >= b) {
			System.out.println("a is greater then or equal to b");
		}
		else {
			System.out.println("a is neither greater nor equals to b ");
		}
	}
}
