
public class SwitchStatement {
	public static void main (String args[]) {
		int a = 20;
		switch (20) {
		case 10 : System.out.println("a value is 10");
		break;
		case 20 : System.out.println("a value is 20");
		break;
		case 30 : System.out.println("a value is 30");
		break;
		default : System.out.println("a value is Not 10,20 or 30");
		}
	}
}
