
public class CoditionalStatement {
	public static void main (String args[]) {
		int a = 10;
		int b = 20;
		int c = 30;
		int d =40;
		if (a > b) {
			System.out.println("a is greater than b");
		}
		else if (c < d) {
			System.out.println("c is less than d");
		}
		else
		{
			System.out.println("Neither (a,b) or (c,d)");
		}
	}
}
